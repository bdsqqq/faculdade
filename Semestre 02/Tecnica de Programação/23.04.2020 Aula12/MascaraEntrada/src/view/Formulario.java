    private Paciente montaPaciente() {
        Paciente pac = new Paciente();
        pac.setPaciente(txtPaciente.getText());
        pac.setNascimento(txtNascimento.getText());
        pac.setSexo(cmbSexo.getSelectedItem().toString());
        return pac;
    }

    public void carregarLista() {
        String titulos[] = {"Código", "Paciente", "Nascimento", "Sexo"};
        DefaultTableModel modelo = new DefaultTableModel(null, titulos);
        ArrayList<Paciente> lista = new PacienteDAO().listaPacientes();
        for (int i = 0; i < lista.size(); i++) {
            modelo.addRow(new Object[]{
                lista.get(i).getCodigo(),
                lista.get(i).getPaciente(),
                lista.get(i).getNascimento(),
                lista.get(i).getSexo()
            });
        }
        tblPacientes.setModel(modelo);
    }

