    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtPaciente.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencher nome do paciente");
        } else if (txtNascimento.getText().equals(" / / ")) {
            JOptionPane.showMessageDialog(null, "Preencher data de nascimento");
        } else {
            Paciente pac = this.montaPaciente();
            String gravar = new PacienteDAO().gravarPaciente(pac);
            if (gravar.equals("OK")) {
                carregarLista();
                JOptionPane.showMessageDialog(null, "Paciente Salvo com sucesso");
            } else {
                JOptionPane.showMessageDialog(null, "Falha ao gravar paciente: " + gravar);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

