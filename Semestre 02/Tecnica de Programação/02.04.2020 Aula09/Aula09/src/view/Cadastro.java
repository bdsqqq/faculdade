    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Funcionario func = new FuncionarioDAO().buscaFuncionario(txtCpf.getText());
        
        if (func == null){
            JOptionPane.showMessageDialog(null, "Funcionario n√£o encontrado");
        } else {
            txtCpf.setText(func.getCpf());
            txtNome.setText(func.getNome());
            txtSalario.setText(func.getSalario()+ "");
            if(func.getSexo() == 'M'){
                optMasculino.setSelected(true);
            } else {
                optFeminino.setSelected(true);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

